  Widget _buildStatusChip(String status) {
    Color color;
    String text;
    final user = ref.read(currentUserProvider);
    
    switch (status) {
      case 'MEMBER_REQUESTED':
        color = Colors.orange;
        text = user?.userType == UserType.member ? 'ÏäπÏù∏ ÎåÄÍ∏∞' : 'ÌöåÏõê ÏäπÏù∏ ÎåÄÍ∏∞';
        break;
      case 'SCHEDULED':
        color = Colors.green;
        text = 'ÏòàÏïΩÌôïÏ†ï';
        break;
      case 'COMPLETED':
        color = Colors.blue;
        text = 'ÏôÑÎ£å';
        break;
      case 'CANCELLED':
        color = Colors.red;
        text = 'Ï∑®ÏÜå';
        break;
      case 'CHANGE_REQUESTED':
        color = Colors.purple;
        text = 'ÌöåÏõê Î≥ÄÍ≤Ω ÏöîÏ≤≠';
        break;
      case 'TRAINER_CHANGE_REQUESTED':
        color = Colors.indigo;
        text = 'Ìä∏Î†àÏù¥ÎÑà Î≥ÄÍ≤Ω ÏöîÏ≤≠';
        break;
      default:
        color = Colors.grey;
        text = status;
    }
    
    return Container(
      padding: const EdgeInsets.symmetric(horizontal: 12, vertical: 6),
      decoration: BoxDecoration(
        color: color.withOpacity(0.1),
        borderRadius: BorderRadius.circular(20),
      ),
      child: Text(
        text,
        style: TextStyle(
          color: color,
          fontSize: 12,
          fontWeight: FontWeight.w600,
        ),
      ),
    );
  }
  
